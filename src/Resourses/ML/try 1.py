a=int(input("Enter first value"))
b=int(input("Enter second value"))

#arithemtic operator
print("\nArithmetic Operator")
print(a+b)
print(a-b)
print(a*b)
print(a/b)
print(a%b)
print(a**b)
print(a//b)

#relational operator
print("\nRelational Operator")
print(a==b)
print(a!=b)
print(a>b)
print(a<b)
print(a>=b)
print(a<=b)

#logical operator
print("\nLogical Operators")
print(a<5 and a>10)
print(a<5 and a<10)
print(a>5 or a<2)
print(a<2 or a<10)
print(not(a>5 and a>10))
